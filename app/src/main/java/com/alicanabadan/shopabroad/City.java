public class City {

    private String name;
    private String code;
    private String country;
    private String shortNameTR;
    private String shortNameEN;
    private String shortNameDE;
    private String shortNameFR;
    private String shortNameRU;
    private String shortNameNL;
    private String shortNameDA;
    private String shortNameIT;
    private String shortNameHE;
    private String shortNameSV;

    public City() {
    }

    public City(String name, String code) {
        this.name = name;
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getShortNameTR() {
        return shortNameTR;
    }

    public void setShortNameTR(String shortNameTR) {
        this.shortNameTR = shortNameTR;
    }

    public String getShortNameEN() {
        return shortNameEN;
    }

    public void setShortNameEN(String shortNameEN) {
        this.shortNameEN = shortNameEN;
    }

    public String getShortNameDE() {
        return shortNameDE;
    }

    public void setShortNameDE(String shortNameDE) {
        this.shortNameDE = shortNameDE;
    }

    public String getShortNameFR() {
        return shortNameFR;
    }

    public void setShortNameFR(String shortNameFR) {
        this.shortNameFR = shortNameFR;
    }

    public String getShortNameRU() {
        return shortNameRU;
    }

    public void setShortNameRU(String shortNameRU) {
        this.shortNameRU = shortNameRU;
    }

    public String getShortNameNL() {
        return shortNameNL;
    }

    public void setShortNameNL(String shortNameNL) {
        this.shortNameNL = shortNameNL;
    }

    public String getShortNameDA() {
        return shortNameDA;
    }

    public void setShortNameDA(String shortNameDA) {
        this.shortNameDA = shortNameDA;
    }

    public String getShortNameIT() {
        return shortNameIT;
    }

    public void setShortNameIT(String shortNameIT) {
        this.shortNameIT = shortNameIT;
    }

    public String getShortNameHE() {
        return shortNameHE;
    }

    public void setShortNameHE(String shortNameHE) {
        this.shortNameHE = shortNameHE;
    }

    public String getShortNameSV() {
        return shortNameSV;
    }

    public void setShortNameSV(String shortNameSV) {
        this.shortNameSV = shortNameSV;
    }
}
